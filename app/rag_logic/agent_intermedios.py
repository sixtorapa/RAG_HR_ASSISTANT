class SQLAgent:
    """
    Agente de consultas al Data Lake / Redshift.
    - SQLDatabaseTool ejecuta la query y devuelve un resultado (texto / tabla renderizada).
    - El LLM interpreta ese resultado en lenguaje de negocio: conclusiones, advertencias, etc.
    """

    def __init__(
        self,
        tool: SQLDatabaseTool,
        model_name: str = "gpt-4o",
        temperature: float = 0.2,
        callbacks: Optional[list] = None,
    ) -> None:
        self.tool = tool
        self.name = tool.name
        self.callbacks = callbacks or []
        self.llm = ChatOpenAI(model_name=model_name, temperature=temperature, callbacks=self.callbacks)

    def run(self, query: str) -> AgentResult:
        raw = self.tool.run({"query": query}, callbacks=self.callbacks)
        result = _normalize_result(raw)

        sql_output = (result.get("answer") or "").strip()

        # ✅ Guardamos SIEMPRE el resultado bruto completo para poder renderizarlo al final
        result["sql_raw_output"] = sql_output

        # (Opcional pero recomendable) evitamos meter tablas gigantes al LLM
        MAX_SQL_CHARS_FOR_LLM = 12000
        sql_output_for_llm = sql_output if len(sql_output) <= MAX_SQL_CHARS_FOR_LLM else (
            sql_output[:MAX_SQL_CHARS_FOR_LLM]
            + "\n... [resultado SQL recortado solo para el análisis del LLM]"
        )

        system_prompt = (
            "You are a DATA ANALYST with a business focus.\n"
            "You receive the result of an already-executed SQL query (may be in text or table format).\n"
            "Your task is to interpret that result for a NON-technical person.\n"
            "Do not invent data that is not in the output. If something cannot be determined, say so.\n"
            "\n"
            "IMPORTANT (visualisation):\n"
            "- Always end your response with a '📊 Recommended Charts' section.\n"
            "- Rule 1: if there is a time column (date/day/month/week), recommend a LINE chart.\n"
            "- Rule 2: if there is no time column and few categories (<= 10), recommend a BAR chart.\n"
            "- Rule 3: if there are many categories, recommend BARS with 'Top N' and specify the criterion.\n"
            "- For each chart specify: (1) type, (2) X axis, (3) Y axis/metric, (4) segmentation/series if applicable.\n"
            "- Do not invent columns: use only those present in the output.\n"
        )
        user_prompt = (
            f"Original query (summary, do not reprocess):\n{query}\n\n"
            f"Raw result returned by the SQL tool:\n{sql_output_for_llm}\n\n"
            "Task:\n"
            "- Explain what these results mean in plain language.\n"
            "- Highlight totals, comparisons and any obvious insights.\n"
            "- Indicate if context is missing (e.g., no historical data, no benchmark).\n"
            "- Avoid mentioning SQL code or internal database details.\n"
            "- If the number of rows does not cover the full period, state this explicitly.\n"
            "- Do not invent tables or values. Do not paste the full dataset.\n"
            "\n"
            "Also add at the end EXACTLY one section:\n"
            "📊 Recommended Charts:\n"
            "- (Chart 1) Type: ... | X: ... | Y: ... | Series/segment: ...\n"
            "- (Chart 2) Type: ... | X: ... | Y: ... | Series/segment: ... (if applicable)\n"
        )
        refined = self.llm.invoke(
            [SystemMessage(content=system_prompt), HumanMessage(content=user_prompt)],
        )

        # ✅ El answer será el insight, pero el dato completo queda en sql_raw_output
        result["answer"] = refined.content.strip()
        return result


