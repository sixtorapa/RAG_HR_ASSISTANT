class DocumentQAAgent:
    """
    Agente de preguntas/respuestas sobre documentos del vector store.
    - Usa ChatWithDocumentTool para recuperar contexto + respuesta preliminar.
    - Después pasa por un LLM con un prompt de "analista documental senior"
      para:
        * Verificar coherencia con el contexto.
        * Completar/limitar la respuesta.
                * Restate it clearly for a business user.
    """

    def __init__(
        self,
        tool: ChatWithDocumentTool,
        model_name: str = "gpt-4o-mini",
        temperature: float = 0.2,
        callbacks: Optional[list] = None,
    ) -> None:
        self.tool = tool
        self.name = tool.name
        self.callbacks = callbacks or []
        

        cb_manager = CallbackManager(self.callbacks) if self.callbacks else None
        self.llm = ChatOpenAI(model=model_name, temperature=temperature, callback_manager=cb_manager)


    def run(
        self,
        question: str,
        chat_history: ChatHistory,
        extra_documents: Optional[List[Any]] = None,
    ) -> AgentResult:

        # 1) Recuperación + respuesta preliminar
        raw = self.tool.run(
            {"question": question, "chat_history": chat_history},
            callbacks=self.callbacks,
        )

        result = _normalize_result(raw)

        # 2) Contexto textual a partir de los documentos
        docs = result.get("source_documents", [])

        # 🔹 Inyectar memoria conversacional como contexto adicional
        if extra_documents:
            docs = list(extra_documents) + list(docs)

        context_text = _build_context_from_docs(docs)


        # 3) Re-razonamiento con un rol claro
        system_prompt = (
            "You are a SENIOR HR DOCUMENT ANALYST.\n"
            "Your job is to answer the user's question ONLY using the information "
            "present in the provided context fragments.\n\n"
            "KEY RULES:\n"
            "- Always respond in clear, professional English.\n"
            "- Do not mention technical concepts like 'vector store', 'chunks', 'embeddings', etc.\n"
            "- If something is not in the context, say so explicitly (e.g., "
            "'this information does not appear explicitly in the reviewed documents').\n"
            "- Never invent figures, dates or conclusions not supported by the context.\n"
            "- If there are multiple related pieces of information, synthesise them clearly "
            "for a business user, not a technical audience.\n"
        )
        user_prompt = (
            f"User question:\n{question}\n\n"
            f"Preliminary answer generated by the system:\n{result.get('answer', '').strip()}\n\n"
            "Relevant context fragments (may include metadata and excerpts from PDFs/Excel):\n"
            f"{context_text}\n\n"
            "Task:\n"
            "- Review the preliminary answer in light of the context.\n"
            "- Correct, complete or qualify the answer if necessary.\n"
            "- If the information is incomplete or ambiguous, explain clearly what is missing.\n"
            "- Return a single, clear final answer for the user.\n"
        )
        refined = self.llm.invoke(
            [SystemMessage(content=system_prompt), HumanMessage(content=user_prompt)],
        )
        result["answer"] = refined.content.strip()
        return result


