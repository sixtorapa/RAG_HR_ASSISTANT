class SummaryAgent:
    def __init__(
        self,
        tool: SummarizeDocumentTool,
        model_name: str = "gpt-4o-mini",
        temperature: float = 0.2,
        callbacks: Optional[list] = None,
    ) -> None:
        self.tool = tool
        self.name = tool.name
        self.callbacks = callbacks or []
        self.llm = ChatOpenAI(model_name=model_name, temperature=temperature, callbacks=self.callbacks)

    def run(self) -> AgentResult:
        raw = self.tool.run({}, callbacks=self.callbacks)
        result = _normalize_result(raw)


        base_summary = result.get("answer", "").strip()

        system_prompt = (
            "You are a SENIOR CONSULTANT. You will receive a raw summary and must transform it into a "
            "DETAILED SUMMARY (not a short one) for a business user.\n"
            "Rules:\n"
            "- Clear, professional English.\n"
            "- Preserve and expand useful detail: figures, dates, percentages and comparisons where available.\n"
            "- Do not invent anything.\n"
            "- Do not cut content: the goal is for the user to fully understand the document.\n"
        )
        user_prompt = (
            "Raw summary generated by the system:\n"
            f"{base_summary}\n\n"
            "Rewrite and structure the content using this format:\n"
            "1) What it is about (3-6 bullet points)\n"
            "2) Main sections/topics (list)\n"
            "3) Key findings (10-20 bullet points)\n"
            "4) Relevant data/metrics (if present)\n"
            "5) Implications (5-10)\n"
            "6) Recommendations (6-12)\n"
            "If information is missing, state it explicitly.\n"
        )
        refined = self.llm.invoke(
            [SystemMessage(content=system_prompt), HumanMessage(content=user_prompt)],
        )
        result["answer"] = refined.content.strip()
        return result


